#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = ft_strlen(dst);
	while ((i < size) && (src[i] != 0))
		dst[j++] = src[i++];
	return (ft_strlen(src) + ft_strlen(dst));
}
